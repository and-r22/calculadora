
public class Calculadora 
{
	
	public static int Suma (int x, int y)
	{
		return x + y;
	}

	public static int Resta (int x, int y)
	{
		return x - y;
	}
	
	public static int Multi (int x, int y)
	{
		return x * y;
	}
	
	public static int Div (int x, int y)
	{
		return x / y;
	}
	
	public static int Suma2 (int x, int y)
	{
		return x + y;
	}


}
